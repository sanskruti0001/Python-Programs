# Number 
num_int = 77
print(num_int)
print(type(num_int))

# String
str = "Sanskruti"
print(str)
print(type(str))

# Float
num_float = 16.3
print(num_float)
print(type(num_float))

# Complex
num_comp = 7+6j
print(num_comp)
print(type(num_comp))

# Boolean

num_variable = True
print(num_variable)
print(type(num_variable))

# Set

my_set = {91,92,93,94,95,96,97,98,99,100}
print(my_set)
print(type(my_set))

